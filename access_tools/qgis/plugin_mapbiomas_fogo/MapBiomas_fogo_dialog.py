    def upload_selected_user_geometry_territory(self, api_client, region_name="brazil"):
        """Exporta a camada poligonal selecionada em ZIP e envia para /territories/upload.

        Retorna o territoryId (UUID) criado pela API ou None quando nao ha
        camada de geometria selecionada no combo.
        """
        if not hasattr(self, "mMapLayerComboBox_usergeometry"):
            return None

        layer = self.mMapLayerComboBox_usergeometry.currentLayer()
        if layer is None:
            return None
        if not isinstance(layer, QgsVectorLayer):
            return None
        if layer.geometryType() != _POLYGON_GEOMETRY:
            return None
        if layer.featureCount() == 0:
            return None

        temp_dir = tempfile.mkdtemp(prefix="mapbiomas_fogo_geometry_")
        try:
            shp_path = os.path.join(temp_dir, "my_geometry.shp")
            
            selected_only_checkbox = False
            if hasattr(self, "SelectedGeometry"):
                selected_only_checkbox = self.SelectedGeometry.isChecked()

            if selected_only_checkbox:
                # Condição 3: SelectedGeometry marcado -> Aplica Dissolve apenas nas geometrias selecionadas pelo usuário
                if layer.selectedFeatureCount() == 0:
                    raise RuntimeError("Nenhuma geometria selecionada na camada para exportar.")
                
                temp_input_shp = os.path.join(temp_dir, "temp_input.shp")
                write_result = QgsVectorFileWriter.writeAsVectorFormat(
                    layer,
                    temp_input_shp,
                    "UTF-8",
                    layer.crs(),
                    "ESRI Shapefile",
                    True,  # only_selected = True
                )

                if isinstance(write_result, tuple):
                    error_code = write_result[0]
                else:
                    error_code = write_result

                if error_code != QgsVectorFileWriter.NoError:
                    raise RuntimeError(f"Falha ao exportar geometrias selecionadas para shapefile temporario. Codigo: {error_code}")

                import processing
                dissolve_params = {
                    'INPUT': temp_input_shp,
                    'FIELD': [],
                    'OUTPUT': shp_path
                }
                processing.run("native:dissolve", dissolve_params)

            else:
                # SelectedGeometry desmarcado
                feature_count = layer.featureCount()

                if feature_count > 1:
                    # Condição 2: Mais de uma feição na camada -> Dissolve toda a camada e segue processo normal
                    temp_input_shp = os.path.join(temp_dir, "temp_input.shp")
                    write_result = QgsVectorFileWriter.writeAsVectorFormat(
                        layer,
                        temp_input_shp,
                        "UTF-8",
                        layer.crs(),
                        "ESRI Shapefile",
                        False,  # only_selected = False
                    )

                    if isinstance(write_result, tuple):
                        error_code = write_result[0]
                    else:
                        error_code = write_result

                    if error_code != QgsVectorFileWriter.NoError:
                        raise RuntimeError(f"Falha ao exportar geometrias da camada para shapefile temporario. Codigo: {error_code}")

                    import processing
                    dissolve_params = {
                        'INPUT': temp_input_shp,
                        'FIELD': [],
                        'OUTPUT': shp_path
                    }
                    processing.run("native:dissolve", dissolve_params)

                else:
                    # Condição 1: Camada com uma única feição -> Processo normal (exporta sem dissolve)
                    write_result = QgsVectorFileWriter.writeAsVectorFormat(
                        layer,
                        shp_path,
                        "UTF-8",
                        layer.crs(),
                        "ESRI Shapefile",
                        False,  # only_selected = False
                    )

                    if isinstance(write_result, tuple):
                        error_code = write_result[0]
                    else:
                        error_code = write_result

                    if error_code != QgsVectorFileWriter.NoError:
                        raise RuntimeError(f"Falha ao exportar geometria unica para shapefile. Codigo: {error_code}")

            base_name = os.path.splitext(shp_path)[0]
            zip_path = os.path.join(temp_dir, "my_geometry.zip")
            has_file = False
            with zipfile.ZipFile(zip_path, "w", zipfile.ZIP_DEFLATED) as zip_file:
                for extension in (".shp", ".shx", ".dbf", ".prj", ".cpg"):
                    part_path = base_name + extension
                    if not os.path.exists(part_path):
                        continue
                    zip_file.write(part_path, arcname=os.path.basename(part_path))
                    has_file = True

            if not has_file:
                raise RuntimeError("Arquivo ZIP da geometria foi criado vazio.")

            territory_id = api_client.upload_territory_zip(zip_path, region_name=region_name)
            print(f"Geometria customizada enviada com sucesso. territoryId: {territory_id}")
            return territory_id
        finally:
            shutil.rmtree(temp_dir, ignore_errors=True)

    def _create_tree_item(self, text, kind, territory_id=None):
        item = QtWidgets.QTreeWidgetItem([text])
        item.setData(0, self.NODE_KIND_ROLE, kind)
        if territory_id:
            item.setData(0, self.TERRITORY_ID_ROLE, territory_id)
        item.setFlags(item.flags() | Qt.ItemIsUserCheckable)
        item.setCheckState(0, Qt.Unchecked)
        return item

    def _add_placeholder(self, parent_item):
        placeholder = QtWidgets.QTreeWidgetItem(["(carregar)"])
        placeholder.setData(0, self.NODE_KIND_ROLE, "placeholder")
        placeholder.setFlags(Qt.ItemIsEnabled)
        parent_item.addChild(placeholder)

    def _has_placeholder(self, item):
        for index in range(item.childCount()):
            if item.child(index).data(0, self.NODE_KIND_ROLE) == "placeholder":
                return True
        return False

    def _remove_placeholders(self, item):
        index = item.childCount() - 1
        while index >= 0:
            child = item.child(index)
            if child.data(0, self.NODE_KIND_ROLE) == "placeholder":
                item.removeChild(child)
            index -= 1

