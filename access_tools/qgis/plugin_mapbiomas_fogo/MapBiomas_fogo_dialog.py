    def _set_children_check_state(self, item, check_state):
        for index in range(item.childCount()):
            child = item.child(index)
            if child.data(0, self.NODE_KIND_ROLE) == "placeholder":
                continue
            child.setCheckState(0, check_state)
            self._set_children_check_state(child, check_state)

    def _update_parent_check_state(self, item):
        if item is None:
            return

        child_states = []
        for index in range(item.childCount()):
            child = item.child(index)
            if child.data(0, self.NODE_KIND_ROLE) == "placeholder":
                continue
            child_states.append(child.checkState(0))

        if not child_states:
            self._update_parent_check_state(item.parent())
            return

        if all(state == Qt.Checked for state in child_states):
            item.setCheckState(0, Qt.Checked)
        elif all(state == Qt.Unchecked for state in child_states):
            item.setCheckState(0, Qt.Unchecked)
        else:
            item.setCheckState(0, Qt.PartiallyChecked)

        self._update_parent_check_state(item.parent())

