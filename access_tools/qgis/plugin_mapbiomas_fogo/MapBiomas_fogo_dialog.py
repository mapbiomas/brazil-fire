# Detect QGIS version and load correct .ui file
def _get_ui_file_path(base_name):
    """Load .ui file compatible with current QGIS version (3 or 4)"""
    try:
        from qgis.core import Qgis as _Qgis
        qgis_version = int(_Qgis.QGIS_VERSION.split('.')[0])
        if qgis_version >= 4:
            ui_file = f'{base_name}_QGS4.ui'
        else:
            ui_file = f'{base_name}_QGS3.ui'
    except (AttributeError, ValueError):
        # Fallback to QGS3 if version detection fails
        ui_file = f'{base_name}_QGS3.ui'
    
    ui_path = os.path.join(os.path.dirname(__file__), ui_file)
    if not os.path.exists(ui_path):
        # Last resort: try without version suffix
        ui_path = os.path.join(os.path.dirname(__file__), f'{base_name}.ui')
    
    return ui_path

