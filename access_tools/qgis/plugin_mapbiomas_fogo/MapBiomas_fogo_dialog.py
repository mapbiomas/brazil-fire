    def _update_brazil_visual_state(self):
        if self._has_user_geometry_selected():
            desired_state = Qt.Unchecked
        else:
            desired_state = (
                Qt.PartiallyChecked
                if self._has_non_brazil_selection()
                else Qt.Checked
            )

        if self._Brasil_root.checkState(0) != desired_state:
            self._Brasil_root.setCheckState(0, desired_state)

