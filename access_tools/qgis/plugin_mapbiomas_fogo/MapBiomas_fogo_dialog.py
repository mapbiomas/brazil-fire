    def _has_non_brazil_selection(self):
        tree = self.Territorial_TreeWidget
        for index in range(tree.topLevelItemCount()):
            item = tree.topLevelItem(index)
            if item is self._Brasil_root:
                continue
            if item.checkState(0) != Qt.Unchecked:
                return True
