    def _populate_uc_types(self, root_item):
        sorted_subcategories = sorted(
            self.UC_SUBCATEGORY_DEFINITIONS,
            key=lambda item: self._normalize_label(item[0]),
        )
        for type_label, match_terms in sorted_subcategories:
            type_item = self._create_tree_item(type_label, "uc_type")
            type_item.setData(0, self.UC_TYPE_ROLE, list(match_terms))
            self._add_placeholder(type_item)
            root_item.addChild(type_item)

    def _list_uc_territories(self):
        if self._uc_territories_cache is not None:
            return self._uc_territories_cache

        dedup = {}
        for category_id in self.UC_CATEGORY_IDS:
            for territory in self._list_territories_sorted(category_id=category_id):
                territory_id = territory.get("id")
                if territory_id:
                    dedup[territory_id] = territory

        self._uc_territories_cache = sorted(
            dedup.values(),
            key=lambda item: self._normalize_label(item.get("name", "")),
        )
        return self._uc_territories_cache

    @staticmethod
    def _tokenize_text(value):
        return [token for token in re.sub(r"[^A-Z0-9]+", " ", value).split() if token]

    def _is_uc_type_match(self, territory_name, match_terms):
        normalized = self._normalize_label(territory_name)
        normalized_tokens = set(self._tokenize_text(normalized))

        for term in match_terms or []:
            normalized_term = self._normalize_label(term)
            if " " in normalized_term:
                if normalized_term in normalized:
                    return True
                continue

            if normalized_term in normalized_tokens:
                return True

        return False

    def _populate_uc_type_territories(self, type_item):
        match_terms = type_item.data(0, self.UC_TYPE_ROLE) or []
        for territory in self._list_uc_territories():
            territory_name = territory.get("name", "Sem nome")
            if not self._is_uc_type_match(territory_name, match_terms):
                continue

            child = self._create_tree_item(
                territory_name,
                "territory",
                territory_id=territory.get("id"),
            )
            type_item.addChild(child)

