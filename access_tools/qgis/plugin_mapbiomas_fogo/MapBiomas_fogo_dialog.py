    def _load_fgb_layer(self, year, territory_ids=None, user_geometry_layer=None, api_client=None):
        """Carrega a camada FlatGeobuf para um ano específico
        
        Se territory_ids for fornecido (e não for Brasil) ou user_geometry_layer for fornecido,
        realiza intersect com a geometria do território ou geometria customizada.
        
        Args:
            year: Ano das feições de fogo
            territory_ids: Lista de IDs de territórios selecionados
            user_geometry_layer: Camada QgsVectorLayer com geometria customizada
            api_client: Cliente API para obter geometria dos territórios
        """
        try:
            # URL pública do arquivo FlatGeobuf no Google Cloud Storage
            url_fgb = f"https://storage.googleapis.com/mapbiomas-public/initiatives/brasil/collection_10/fire-col5/annual_burned_vectors_v1/FlatGeobuf/mapbiomas_fire_collection5_burned_area_{year}.fgb"
            
            # O prefixo /vsicurl/ instrui o GDAL a ler o arquivo remoto sob demanda (via HTTP Range Requests)
            path_vsicurl = f"/vsicurl/{url_fgb}"
            
            # Instancia a camada usando o provedor OGR (que dá suporte a FlatGeobuf)
            layer = QgsVectorLayer(path_vsicurl, f"MapBiomas Fogo - {year}", "ogr")
            
            if not layer.isValid():
                QgsMessageLog.logMessage(
                    f"Erro ao carregar a camada FlatGeobuf para {year}. Verifique a URL ou conexão de rede.",
                    "MapBiomas Fogo",
                    Qgis.Critical
                )
                return
            
            # Aplica o filtro de atributos ANTES de adicionar à tela para evitar requisições desnecessárias
            layer.setSubsetString(f"year = {year}")
            
            # Verifica se é Brasil (sem territórios ou territoryId = "1-1-1") ou se há geometria customizada
            is_brasil_only = (
                not territory_ids or 
                (isinstance(territory_ids, list) and all(tid == "1-1-1" for tid in territory_ids))
            )
            
            # Se é Brasil e não há geometria customizada, carrega normalmente
            if is_brasil_only and user_geometry_layer is None:
                QgsProject.instance().addMapLayer(layer)
                QgsMessageLog.logMessage(
                    f"Camada FlatGeobuf para {year} carregada e filtrada com sucesso!",
                    "MapBiomas Fogo",
                    Qgis.Info
                )
                return
            
            # Se há geometria customizada, faz intersect com ela
            if user_geometry_layer is not None:
                self._clip_with_user_geometry(layer, user_geometry_layer, year)
                return
            
            # Se há territórios selecionados (não Brasil), faz intersect com geometria da API
            if territory_ids and api_client is not None:
                self._clip_with_territory_geometry(layer, territory_ids, api_client, year)
                return
            
            # Fallback: carrega normalmente
            QgsProject.instance().addMapLayer(layer)
            QgsMessageLog.logMessage(
                f"Camada FlatGeobuf para {year} carregada com sucesso!",
                "MapBiomas Fogo",
                Qgis.Info
            )
            
        except Exception as exc:
            QgsMessageLog.logMessage(
                f"Erro ao carregar camada para {year}: {str(exc)}",
                "MapBiomas Fogo",
                Qgis.Critical
            )

    @staticmethod
    def _build_qgs_geometry_from_geojson(geometry_payload):
        """Converte GeoJSON em QgsGeometry com fallback para versoes antigas do QGIS."""
        if not isinstance(geometry_payload, dict):
            return None

        geometry_json = json.dumps(geometry_payload, ensure_ascii=False)

        # QGIS mais novo pode ter QgsGeometry.fromGeoJson.
        from_geojson = getattr(QgsGeometry, "fromGeoJson", None)
        if callable(from_geojson):
            for candidate in (geometry_json.encode("utf-8"), geometry_json):
                try:
                    geometry = from_geojson(candidate)
                    if geometry is not None and not geometry.isEmpty():
                        return geometry
                except Exception:
                    pass

        # Fallback compativel com QGIS antigos via OGR -> WKT -> QgsGeometry.
        try:
            from osgeo import ogr

            ogr_geometry = ogr.CreateGeometryFromJson(geometry_json)
            if ogr_geometry is None:
                print("[MapBiomas Fogo] Falha ao converter GeoJSON para OGR geometry.")
                return None

            geometry_wkt = ogr_geometry.ExportToWkt()
            if not geometry_wkt:
                print("[MapBiomas Fogo] OGR geometry sem WKT para conversao.")
                return None

            geometry = QgsGeometry.fromWkt(geometry_wkt)
            if geometry is None or geometry.isEmpty():
                print("[MapBiomas Fogo] Falha ao converter WKT em QgsGeometry.")
                return None

            return geometry
        except Exception as exc:
            print(f"[MapBiomas Fogo] Falha no fallback OGR para QgsGeometry: {exc}")
            return None

    def _clip_with_territory_geometry(self, fgb_layer, territory_ids, api_client, year):
        """Realiza intersect entre FGB e geometria de territórios da API
        
        Args:
            fgb_layer: Camada QgsVectorLayer com feições de fogo (FGB)
            territory_ids: Lista de IDs de territórios
            api_client: Cliente MapBiomasApiClient para obter geometria
            year: Ano das feições para nome da camada
        """
        try:
            print(f"[MapBiomas Fogo] Iniciando intersect com territórios: {territory_ids}")
            
            # Obtém a geometria dos territórios da API
            territory_geometry = api_client.get_territory_geometry(
                region_name="brazil",
                territoryIds=territory_ids,
            )
            
            if not territory_geometry:
                QgsMessageLog.logMessage(
                    f"Não foi possível obter geometria dos territórios: {territory_ids}",
                    "MapBiomas Fogo",
                    Qgis.Warning
                )
                # Fallback: carrega normalmente
                QgsProject.instance().addMapLayer(fgb_layer)
                return
            
            # Converte geometria GeoJSON da API para QgsGeometry
            territory_qgs_geom = self._build_qgs_geometry_from_geojson(territory_geometry)
            
            if territory_qgs_geom is None or territory_qgs_geom.isEmpty():
                QgsMessageLog.logMessage(
                    f"Geometria do território está vazia: {territory_ids}",
                    "MapBiomas Fogo",
                    Qgis.Warning
                )
                # Fallback: carrega normalmente
                QgsProject.instance().addMapLayer(fgb_layer)
                return
            
            # Realiza o intersect
            self._perform_clip(fgb_layer, territory_qgs_geom, year, "Território")
            
        except Exception as exc:
            QgsMessageLog.logMessage(
                f"Erro ao fazer intersect com territórios: {str(exc)}",
                "MapBiomas Fogo",
                Qgis.Critical
            )
            # Fallback: carrega normalmente
            QgsProject.instance().addMapLayer(fgb_layer)

    def _clip_with_user_geometry(self, fgb_layer, user_geometry_layer, year):
        """Realiza intersect entre FGB e geometria customizada do usuário
        
        Args:
            fgb_layer: Camada QgsVectorLayer com feições de fogo (FGB)
            user_geometry_layer: Camada QgsVectorLayer com geometria customizada
            year: Ano das feições para nome da camada
        """
        try:
            print(f"[MapBiomas Fogo] Iniciando intersect com geometria customizada")
            
            if user_geometry_layer.featureCount() == 0:
                QgsMessageLog.logMessage(
                    "Geometria customizada não possui feições",
                    "MapBiomas Fogo",
                    Qgis.Warning
                )
                # Fallback: carrega normalmente
                QgsProject.instance().addMapLayer(fgb_layer)
                return
            
            # Obtém a geometria (dissolve se houver múltiplas feições)
            selected_only = False
            if hasattr(self, "SelectedGeometry"):
                selected_only = self.SelectedGeometry.isChecked()
            
            user_geom = self._get_dissolved_geometry(user_geometry_layer, selected_only)
            
            if user_geom is None or user_geom.isEmpty():
                QgsMessageLog.logMessage(
                    "Geometria customizada está vazia após processamento",
                    "MapBiomas Fogo",
                    Qgis.Warning
                )
                # Fallback: carrega normalmente
                QgsProject.instance().addMapLayer(fgb_layer)
                return
            
            # Realiza o intersect
            self._perform_clip(fgb_layer, user_geom, year, "Geometria Customizada")
            
        except Exception as exc:
            QgsMessageLog.logMessage(
                f"Erro ao fazer intersect com geometria customizada: {str(exc)}",
                "MapBiomas Fogo",
                Qgis.Critical
            )
            # Fallback: carrega normalmente
            QgsProject.instance().addMapLayer(fgb_layer)

    def _get_dissolved_geometry(self, vector_layer, selected_only=False):
        """Obtém a geometria dissolvida de uma camada vetorial
        
        Args:
            vector_layer: Camada QgsVectorLayer
            selected_only: Se True, usa apenas features selecionadas
            
        Returns:
            QgsGeometry com geometria dissolvida ou None
        """
        try:
            features = []
            
            if selected_only:
                features = vector_layer.selectedFeatures()
            else:
                features = vector_layer.getFeatures()
            
            if not features:
                return None
            
            # Combina todas as geometrias
            geometries = [f.geometry() for f in features if f.geometry() and not f.geometry().isEmpty()]
            
            if not geometries:
                return None
            
            if len(geometries) == 1:
                return geometries[0]
            
            # Faz a união de todas as geometrias
            combined_geom = geometries[0]
            for geom in geometries[1:]:
                combined_geom = combined_geom.combine(geom)
            
            return combined_geom
            
        except Exception as exc:
            print(f"Erro ao obter geometria dissolvida: {exc}")
            return None

    def _perform_clip(self, fgb_layer, clip_geometry, year, clip_source_name):
        """Realiza clip entre camada FGB e geometria de recorte usando algoritmo nativo do QGIS
        
        Args:
            fgb_layer: Camada QgsVectorLayer com feições de fogo
            clip_geometry: QgsGeometry para clipe
            year: Ano para nome da camada
            clip_source_name: Nome da fonte de clipe (para logging)
        """
        try:
            print(f"[MapBiomas Fogo] Realizando clip com {clip_source_name} ({year})")
            
            # Cria uma camada de memória temporária com a geometria de clipe
            clip_layer = QgsVectorLayer("Polygon", "temp_clip", "memory")
            clip_layer.dataProvider().addAttributes([QgsField("id", QVariant.Int)])
            clip_layer.updateFields()
            
            clip_feature = QgsFeature(clip_layer.fields())
            clip_feature.setGeometry(clip_geometry)
            clip_feature.setAttribute(0, 1)
            clip_layer.dataProvider().addFeatures([clip_feature])
            
            # Executa o algoritmo de clip nativo do QGIS (muito mais otimizado)
            try:
                result = processing.run(
                    "native:clip",
                    {
                        'INPUT': fgb_layer,
                        'OVERLAY': clip_layer,
                        'OUTPUT': 'memory:'
                    },
                    context=QgsProcessingContext(),
                    feedback=QgsProcessingFeedback(),
                    is_child_algorithm=False
                )
                
                clipped_layer = result['OUTPUT']
                
                if clipped_layer is None or not isinstance(clipped_layer, QgsVectorLayer):
                    QgsMessageLog.logMessage(
                        f"Falha ao executar algoritmo de clip com {clip_source_name}",
                        "MapBiomas Fogo",
                        Qgis.Warning
                    )
                    QgsProject.instance().addMapLayer(fgb_layer)
                    return
                
                # Renomeia a camada
                clipped_layer.setName(f"MapBiomas Fogo - {year}")
                
                # Valida a camada antes de adicionar
                if not clipped_layer.isValid():
                    QgsMessageLog.logMessage(
                        f"Camada clipeada inválida para {year}",
                        "MapBiomas Fogo",
                        Qgis.Warning
                    )
                    QgsProject.instance().addMapLayer(fgb_layer)
                    return
                
                # Adiciona a camada ao projeto
                QgsProject.instance().addMapLayer(clipped_layer)
                
                feature_count = clipped_layer.featureCount()
                QgsMessageLog.logMessage(
                    f"Clip concluído: {feature_count} feições para {year} ({clip_source_name})",
                    "MapBiomas Fogo",
                    Qgis.Info
                )
                
            except Exception as proc_exc:
                print(f"[MapBiomas Fogo] Erro ao executar algoritmo de clip: {str(proc_exc)}")
                QgsMessageLog.logMessage(
                    f"Erro ao executar algoritmo de clip: {str(proc_exc)}",
                    "MapBiomas Fogo",
                    Qgis.Warning
                )
                # Fallback: carrega a camada sem clipe
                QgsProject.instance().addMapLayer(fgb_layer)
                
        except Exception as exc:
            print(f"[MapBiomas Fogo] Erro ao realizar clip: {str(exc)}")
            QgsMessageLog.logMessage(
                f"Erro ao realizar clip: {str(exc)}",
                "MapBiomas Fogo",
                Qgis.Critical
            )

