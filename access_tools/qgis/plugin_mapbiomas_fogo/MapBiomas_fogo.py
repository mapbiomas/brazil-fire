class _DataBarDelegate(QStyledItemDelegate):
    """Delegate que desenha uma barra de dados no fundo da celula de legenda,
    proporcional ao valor da linha, com a cor da respectiva legenda."""

    # Transparencia da barra (0-255)
    BAR_ALPHA = 110

    def __init__(self, parent=None):
        super().__init__(parent)
        # Lista de (raw_value, hex_color) na mesma ordem das linhas da tabela.
        self._row_data = []
        self._max_value = 1.0

    def set_rows(self, row_data):
        """row_data: list of (raw_value, hex_color)."""
        self._row_data = row_data
        non_total = [v for v, c in row_data if v is not None and c != "transparent"]
        self._max_value = max(non_total) if non_total else 1.0

    def paint(self, painter, option, index):
        row = index.row()
        if row >= len(self._row_data):
            super().paint(painter, option, index)
            return

        raw_value, hex_color = self._row_data[row]
        is_total = (hex_color == "transparent" or raw_value is None)

        painter.save()

        # 1. Desenha a barra antes do texto
        if not is_total and raw_value and self._max_value > 0:
            ratio = min(float(raw_value) / self._max_value, 1.0)
            bar_width = int(option.rect.width() * ratio)
            bar_rect = QRectF(
                option.rect.left(),
                option.rect.top(),
                bar_width,
                option.rect.height(),
            )
            try:
                c = QColor(hex_color)
            except Exception:
                c = QColor("#CCCCCC")
            c.setAlpha(self.BAR_ALPHA)
            painter.fillRect(bar_rect, c)

        # 2. Texto e fundo de selecao por cima da barra (sem duplicar)
        super().paint(painter, option, index)

        painter.restore()


class _StatisticsTableItem(QTableWidgetItem):
    """Item com chave de ordenacao customizada e regra para manter 'Total' por ultimo."""

    ROLE_SORT_VALUE = Qt.UserRole + 11
    ROLE_IS_TOTAL = Qt.UserRole + 12

    def __init__(self, text, sort_value=None, is_total=False):
        super().__init__(text)
        self.setData(self.ROLE_SORT_VALUE, sort_value if sort_value is not None else text)
        self.setData(self.ROLE_IS_TOTAL, bool(is_total))

    def __lt__(self, other):
        if not isinstance(other, QTableWidgetItem):
            return super().__lt__(other)

        self_is_total = bool(self.data(self.ROLE_IS_TOTAL))
        other_is_total = bool(other.data(self.ROLE_IS_TOTAL))
        if self_is_total != other_is_total:
            return not self_is_total

        self_value = self.data(self.ROLE_SORT_VALUE)
        other_value = other.data(self.ROLE_SORT_VALUE)

        number_types = (int, float)
        if isinstance(self_value, number_types) and isinstance(other_value, number_types):
            return float(self_value) < float(other_value)

        return str(self_value or "").casefold() < str(other_value or "").casefold()


class _StatisticsFetchWorker(QObject):
    """Executa consulta de estatistica em thread separada para nao bloquear a UI."""

    finished = pyqtSignal(int, object)
    failed = pyqtSignal(int, str)

    def __init__(self, run_id, api_config, request_data, request_timeout):
        super().__init__()
        self._run_id = run_id
        self._api_config = dict(api_config or {})
        self._request_data = dict(request_data or {})
        self._request_timeout = request_timeout

    def run(self):
        try:
            client = MapBiomasApiClient(
                base_url=self._api_config.get("base_url") or base_url,
                upload_base_url=self._api_config.get("upload_base_url") or upload_base_url,
                timeout=self._request_timeout,
            )
            request_args = dict(self._request_data.get("request_args") or {})
            payload = client.get_area_statistics(
                requestTimeout=self._request_timeout,
                requestMaxAttempts=1,
                **request_args,
            )

            self.finished.emit(
                self._run_id,
                {
                    "payload": payload,
                    "legend_items": list(self._request_data.get("legend_items") or []),
                    "context": dict(self._request_data.get("context") or {}),
                },
            )
        except Exception as exc:
            self.failed.emit(self._run_id, str(exc))


