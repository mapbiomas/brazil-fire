    def run(self):
        """Run method that performs all the real work"""

        # Create the dialog with elements (after translation) and keep reference
        # Only create GUI ONCE in callback, so that it will only load when the plugin is started
        if not hasattr(self, "dlg") or self.dlg is None:
            self.dlg = MapbiomasfogoDialog()
            self.first_start = False

        # show the dialog
        self.dlg.show()
        # Run the dialog event loop
        result = self.dlg.exec()
        # See if OK was pressed
        if result:
            region = "brazil"
            territory_ids = self.dlg.get_selected_territory_ids() or []
            zoom_to_territory = False

            custom_territory_id = None
            try:
                custom_territory_id = self.dlg.upload_selected_user_geometry_territory(
                    self.api_client,
                    region_name=region,
                )
            except Exception as exc:
                warning_message = f"Falha ao enviar geometria customizada para API: {exc}"
                print(warning_message)
                self.iface.messageBar().pushWarning("MapBiomas Fogo", warning_message)

            if custom_territory_id:
                # Com geometria customizada, prioriza o mesmo fluxo da plataforma web.
                territory_ids = [custom_territory_id]
                print(f"Recorte ativo: geometria carregada (territoryId={custom_territory_id})")
            elif territory_ids:
                zoom_to_territory = True
                print(f"Recorte ativo: territorio selecionado ({territory_ids})")
            else:
                # Mantem o fluxo de recorte/zoom usando o territoryId oficial do Brasil.
                territory_ids = ["1-1-1"]
                zoom_to_territory = True
                print("Recorte ativo: Brasil inteiro (territoryId=1-1-1)")

            for request in self._get_layer_requests():
                if not request["enabled"]:
                    continue

                try:
                    self._load_layer_from_api(
                        region=region,
                        subtheme_key=request["subtheme_key"],
                        legend_key=request["legend_key"],
                        year=request["year"],
                        layer_name=request["layer_name"],
                        legend_year=request.get("legend_year"),
                        territory_ids=territory_ids,
                        zoom_to_territory=zoom_to_territory,
                    )
                except Exception as exc:
                    error_msg = f"Erro ao consultar API do MapBiomas para {request['legend_key']}: {exc}"
                    print(error_msg)
                    print("[MapBiomas Fogo] Verifique o console Python para detalhes. Use diagnostic_api.py para diagnosticar o problema.")