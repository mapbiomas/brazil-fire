    def _export_statistics_data(self, dock, layer_name):
        payload = getattr(dock, "_statistics_export_payload", None)
        if not payload:
            self.iface.messageBar().pushWarning(
                "MapBiomas Fogo",
                "Gere a estatistica primeiro antes de exportar.",
            )
            return

        clean_layer_name = self._strip_layer_index(payload.get("layer_name") or layer_name)
        export_layer = QgsVectorLayer("None", f"Estatisticas - {clean_layer_name}", "memory")
        provider = export_layer.dataProvider()

        years = payload.get("year") or []
        year_start = years[0] if years else None
        year_end = years[-1] if years else None
        unit = str(payload.get("unit") or "")

        provider.addAttributes([
            QgsField("camada", QVariant.String),
            QgsField("legenda", QVariant.String),
            QgsField("valor", QVariant.Double),
            QgsField("valor_fmt", QVariant.String),
            QgsField("unidade", QVariant.String),
            QgsField("ano_ini", QVariant.Int),
            QgsField("ano_fim", QVariant.Int),
        ])
        export_layer.updateFields()

        features = []
        for row in payload.get("rows", []):
            raw_value = row.get("valor", 0)
            row_year_start = row.get("ano_ini", year_start)
            row_year_end = row.get("ano_fim", year_end)
            feature = QgsFeature(export_layer.fields())
            feature.setAttributes([
                clean_layer_name,
                str(row.get("legenda") or ""),
                float(raw_value) if raw_value is not None else 0.0,
                self._format_stat_number(raw_value, use_grouping=False),
                unit,
                int(row_year_start) if row_year_start is not None else None,
                int(row_year_end) if row_year_end is not None else None,
            ])
            features.append(feature)

        provider.addFeatures(features)
        previous_active_layer = self.iface.activeLayer()
        export_layer.setCustomProperty("mapbiomas_fogo/temp_export_layer", True)
        QgsProject.instance().addMapLayer(export_layer)
        self.iface.setActiveLayer(export_layer)

        action = self.iface.actionLayerSaveAs()
        try:
            if action is not None:
                action.trigger()
        finally:
            if QgsProject.instance().mapLayer(export_layer.id()) is not None:
                QgsProject.instance().removeMapLayer(export_layer.id())
            if previous_active_layer is not None:
                previous_layer_id = previous_active_layer.id()
                restored_layer = QgsProject.instance().mapLayer(previous_layer_id)
                if restored_layer is not None:
                    self.iface.setActiveLayer(restored_layer)

