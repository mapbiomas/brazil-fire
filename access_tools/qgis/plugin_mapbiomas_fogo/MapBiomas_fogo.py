    @staticmethod
    def _format_stat_number(value, use_grouping=True):
        try:
            format_spec = ",.3f" if use_grouping else ".3f"
            number_text = format(float(value), format_spec)
            if use_grouping:
                return number_text.replace(",", "#").replace(".", ",").replace("#", ".")
            return number_text.replace(".", ",")
        except (TypeError, ValueError):
            return "0,000"

    @staticmethod
    def _format_stat_value(value, unit):
        unit_text = str(unit or "").strip()
        suffix = f" {unit_text}" if unit_text else ""
        return f"{Mapbiomasfogo._format_stat_number(value)}{suffix}"

