    @staticmethod
    def _legend_child_pixel_values(legend_items):
        """Retorna os pixel values que pertencem a itens filhos da legenda."""
        child_pixel_values = set()
        for legend_item in legend_items or []:
            children = legend_item.get("children") or []
            if not isinstance(children, list):
                continue
            for child in children:
                pixel_value = child.get("pixelValue") if isinstance(child, dict) else None
                if pixel_value is not None:
                    child_pixel_values.add(pixel_value)
        return child_pixel_values

