    @staticmethod
    def _extract_year_from_legend_item(legend_item):
        """Extrai o ano de um item de legenda do tipo year_2025."""
        key = legend_item.get("key", "")
        match = re.search(r"year_(\d{4})", str(key))
        if match:
            return int(match.group(1))

        name = legend_item.get("name", {})
        if isinstance(name, dict):
            for value in name.values():
                if str(value).isdigit() and len(str(value)) == 4:
                    return int(value)
        return None

    def _filter_last_year_legend(self, legend_items, pixel_values, selected_year):
        """Mantém apenas os itens da legenda do último ano até o ano selecionado."""
        filtered_items = []
        filtered_pixel_values = []

        for item in legend_items:
            item_year = self._extract_year_from_legend_item(item)
            if item_year is None or item_year <= selected_year:
                filtered_items.append(item)
                if item.get("pixelValue") is not None:
                    filtered_pixel_values.append(item.get("pixelValue"))

        if not filtered_pixel_values:
            filtered_pixel_values = pixel_values

        return filtered_items, filtered_pixel_values

    def _filter_accumulated_legend(self, legend_items, pixel_values, selected_year):
        """Mantém anos de ano_minimo até selected_year, ordenando do mais recente para o mais antigo."""
        try:
            min_year = int(ano_minimo)
        except (TypeError, ValueError):
            min_year = 1985

        rows_with_year = []
        for item in legend_items:
            item_year = self._extract_year_from_legend_item(item)
            if item_year is None:
                continue
            if min_year <= item_year <= int(selected_year):
                rows_with_year.append((item_year, item))

        if not rows_with_year:
            return legend_items, pixel_values

        rows_with_year.sort(key=lambda pair: pair[0], reverse=True)
        ordered_items = [item for _year, item in rows_with_year]
        ordered_pixel_values = [item.get("pixelValue") for item in ordered_items if item.get("pixelValue") is not None]

        if not ordered_pixel_values:
            ordered_pixel_values = pixel_values

        return ordered_items, ordered_pixel_values

    @staticmethod
    def _extract_frequency_count_from_item(legend_item):
        """Extrai a contagem de vezes de um item tipo frequency_5 ou '5 vezes'."""
        key = legend_item.get("key", "")
        match = re.search(r"frequency_(\d+)", str(key))
        if match:
            return int(match.group(1))

        name = legend_item.get("name", {})
        if isinstance(name, dict):
            for value in name.values():
                match = re.search(r"(\d+)", str(value))
                if match:
                    return int(match.group(1))
        return None

    def _filter_frequency_legend(self, legend_items, pixel_values, selected_year):
        """Mantém somente as frequências compatíveis com o ano selecionado."""
        max_frequency = max(1, int(selected_year) - int(ano_minimo))
        filtered_items = []
        filtered_pixel_values = []

        for item in legend_items:
            item_frequency = self._extract_frequency_count_from_item(item)
            if item_frequency is None or item_frequency <= max_frequency:
                filtered_items.append(item)
                if item.get("pixelValue") is not None:
                    filtered_pixel_values.append(item.get("pixelValue"))

        if not filtered_pixel_values:
            filtered_pixel_values = pixel_values

        return filtered_items, filtered_pixel_values

    def _load_layer_from_api(self, region, subtheme_key, legend_key, year, layer_name, legend_year=None, territory_ids=None, zoom_to_territory=False):
        """Consulta pixel values + map URL e adiciona camada raster ao projeto."""
        legend_data = self.api_client.get_pixel_values_by_key(
            legend_key,
            region_name=region,
        )
        pixel_values = legend_data.get("pixelValues", [])
        legend_items = legend_data.get("legendItems", [])

        selected_year = self._resolve_selected_year_for_legend(
            legend_key,
            year,
            legend_year=legend_year,
        )

        if legend_key == "fire_total_burned_last_year" and isinstance(selected_year, int):
            legend_items, pixel_values = self._filter_last_year_legend(
                legend_items,
                pixel_values,
                selected_year,
            )
        elif legend_key == "fire_total_burned_accumulated" and isinstance(selected_year, int):
            legend_items, pixel_values = self._filter_accumulated_legend(
                legend_items,
                pixel_values,
                selected_year,
            )
        elif legend_key == "fire_total_burned_frequency" and isinstance(selected_year, int):
            legend_items, pixel_values = self._filter_frequency_legend(
                legend_items,
                pixel_values,
                selected_year,
            )

        map_url = self.api_client.get_map_url(
            region=region,
            subthemeKey=subtheme_key,
            legendKey=legend_key,
            pixelValue=pixel_values,
            year=year,
            territoryId=territory_ids,
        )

        legend_payload = {
            "region": region,
            "subthemeKey": subtheme_key,
            "legendKey": legend_key,
            "year": year,
            "mapUrl": map_url,
            "legendId": legend_data.get("legendId"),
            "pixelValues": pixel_values,
            "legendItems": legend_items,
            "territoryIds": territory_ids,
        }
        self._save_legend_payload(legend_key, legend_payload)

        if not map_url:
            print(f"API retornou URL vazia para {legend_key}.")
            return

        xyz_uri = f"type=xyz&url={map_url}"
        raster_layer = QgsRasterLayer(xyz_uri, layer_name, "wms")
        if raster_layer.isValid():
            layer_index, indexed_layer_name = self._next_indexed_layer_name(layer_name)
            raster_layer.setName(indexed_layer_name)
            legend_context = {
                "region": region,
                "subthemeKey": subtheme_key,
                "legendKey": legend_key,
                "year": year,
                "pixelValues": pixel_values,
                "territoryIds": territory_ids,
            }
            self._attach_dynamic_legend(raster_layer, legend_items, legend_context)
            QgsProject.instance().addMapLayer(raster_layer)
            print(f"[MapBiomas Fogo] Camada adicionada ao projeto: {indexed_layer_name}")
            self._show_statistics_dock(indexed_layer_name, layer_index, raster_layer.id())
            if zoom_to_territory:
                # QGIS 4 fix: Increase delay to ensure layer is fully rendered before zooming
                # Use 500ms minimum for QGIS 4/Qt6 rendering pipeline
                delay_ms = 500
                print(f"[MapBiomas Fogo] Agendando zoom para territorio em {delay_ms}ms")
                QTimer.singleShot(delay_ms, lambda r=region, tids=territory_ids: self._zoom_canvas_to_territory(r, tids))
        else:
            print(f"Erro ao carregar a camada raster para {legend_key}.")
            print(f"URL retornada: {map_url}")

    def _zoom_canvas_to_territory(self, region, territory_ids):
        """Ajusta o canvas ao limite da geometria do territoryId ativo."""
        print(f"[MapBiomas Fogo] _zoom_canvas_to_territory chamada: region={region}, territory_ids={territory_ids}")
        if not territory_ids:
            print("[MapBiomas Fogo] territory_ids vazio, cancelando zoom")
            return

        try:
            if isinstance(territory_ids, str):
                territory_ids_log = [territory_ids]
            else:
                territory_ids_log = [str(item) for item in territory_ids if str(item).strip()]

            print(f"[MapBiomas Fogo] Consultando geometria da API para: {territory_ids_log}")
            geometry_payload = self.api_client.get_territory_geometry(
                region_name=region,
                territoryIds=territory_ids,
            )
            if not geometry_payload:
                print(
                    "[MapBiomas Fogo] Geometria do territoryId nao retornada -> "
                    f"region={region}, territoryIds={territory_ids_log}"
                )
                return

            print(f"[MapBiomas Fogo] Geometria recebida da API, convertendo para QgsGeometry...")
            geometry = self._build_qgs_geometry_from_geojson(geometry_payload)
            if geometry is None or geometry.isEmpty():
                print(
                    "[MapBiomas Fogo] Geometria do territoryId veio vazia -> "
                    f"region={region}, territoryIds={territory_ids_log}"
                )
                return

            canvas = self.iface.mapCanvas()
            if canvas is None:
                print("[MapBiomas Fogo] mapCanvas nao disponivel")
                return

            source_crs = QgsCoordinateReferenceSystem("EPSG:4326")
            destination_crs = canvas.mapSettings().destinationCrs()
            extent = geometry.boundingBox()
            if destination_crs.isValid() and destination_crs != source_crs:
                transform = QgsCoordinateTransform(source_crs, destination_crs, QgsProject.instance())
                extent = transform.transformBoundingBox(extent)

            extent = QgsRectangle(extent)
            extent.scale(1.05)
            print(
                "[MapBiomas Fogo] Ajustando canvas pelo territoryId -> "
                f"territoryIds={territory_ids_log}, extent={extent.toString()}"
            )
            canvas.setExtent(extent)
            canvas.refresh()
            print("[MapBiomas Fogo] Zoom aplicado com sucesso")
        except Exception as exc:
            print(f"[MapBiomas Fogo] Falha ao ajustar o canvas pelo territoryId: {exc}")
            traceback.print_exc()

    @staticmethod
    def _build_qgs_geometry_from_geojson(geometry_payload):
        """Converte GeoJSON em QgsGeometry com fallback para versoes antigas do QGIS."""
        if not isinstance(geometry_payload, dict):
            return None

        geometry_json = json.dumps(geometry_payload, ensure_ascii=False)

        # QGIS mais novo pode ter QgsGeometry.fromGeoJson.
        from_geojson = getattr(QgsGeometry, "fromGeoJson", None)
        if callable(from_geojson):
            for candidate in (geometry_json.encode("utf-8"), geometry_json):
                try:
                    geometry = from_geojson(candidate)
                    if geometry is not None and not geometry.isEmpty():
                        return geometry
                except Exception:
                    pass

        # Fallback compativel com QGIS antigos via OGR -> WKT -> QgsGeometry.
        try:
            from osgeo import ogr

            ogr_geometry = ogr.CreateGeometryFromJson(geometry_json)
            if ogr_geometry is None:
                print("[MapBiomas Fogo] Falha ao converter GeoJSON para OGR geometry.")
                return None

            geometry_wkt = ogr_geometry.ExportToWkt()
            if not geometry_wkt:
                print("[MapBiomas Fogo] OGR geometry sem WKT para conversao.")
                return None

            geometry = QgsGeometry.fromWkt(geometry_wkt)
            if geometry is None or geometry.isEmpty():
                print("[MapBiomas Fogo] Falha ao converter WKT em QgsGeometry.")
                return None

            return geometry
        except Exception as exc:
            print(f"[MapBiomas Fogo] Falha no fallback OGR para QgsGeometry: {exc}")
            return None

