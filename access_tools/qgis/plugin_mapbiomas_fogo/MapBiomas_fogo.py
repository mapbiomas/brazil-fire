    def _refresh_statistics_table_data_bars(self, table_widget):
        """Atualiza os dados da barra para refletir a ordem atual das linhas."""
        delegate = getattr(table_widget, "_bar_delegate", None)
        if delegate is None:
            return

        row_data = []
        for row in range(table_widget.rowCount()):
            label_item = table_widget.item(row, 1)
            if label_item is None:
                row_data.append((None, "transparent"))
                continue
            raw_value = label_item.data(Qt.UserRole + 1)
            color = label_item.data(Qt.UserRole + 2) or "transparent"
            row_data.append((raw_value, color))

        delegate.set_rows(row_data)
        table_widget.viewport().update()

    def _render_statistics_table_rows(self, table_widget, table_rows):
        """Renderiza linhas da tabela sem usar sort nativo do QTableWidget."""
        table_widget.setRowCount(len(table_rows))
        for row_index, row_data in enumerate(table_rows):
            color = row_data.get("color", "transparent")
            raw_value = row_data.get("raw_value", 0.0)
            label_text = row_data.get("label", "")

            color_item = QTableWidgetItem("")
            if color == "transparent":
                color_item.setBackground(QColor(0, 0, 0, 0))
            else:
                color_item.setBackground(QColor(color))
            color_item.setFlags(color_item.flags() & ~Qt.ItemIsEditable)

            label_item = QTableWidgetItem(str(label_text))
            label_item.setData(Qt.UserRole + 1, raw_value)
            label_item.setData(Qt.UserRole + 2, color)
            label_item.setFlags(label_item.flags() & ~Qt.ItemIsEditable)

            value_item = QTableWidgetItem(str(row_data.get("value_text", "")))
            value_item.setTextAlignment(Qt.AlignRight | Qt.AlignVCenter)
            value_item.setFlags(value_item.flags() & ~Qt.ItemIsEditable)

            unit_item = QTableWidgetItem(str(row_data.get("unit_text", "")))
            unit_item.setTextAlignment(Qt.AlignCenter | Qt.AlignVCenter)
            unit_item.setFlags(unit_item.flags() & ~Qt.ItemIsEditable)

            if row_data.get("is_total"):
                font = label_item.font()
                font.setBold(True)
                label_item.setFont(font)
                value_item.setFont(font)
                unit_item.setFont(font)

            table_widget.setItem(row_index, 0, color_item)
            table_widget.setItem(row_index, 1, label_item)
            table_widget.setItem(row_index, 2, value_item)
            table_widget.setItem(row_index, 3, unit_item)

        self._refresh_statistics_table_data_bars(table_widget)

    @staticmethod
    def _try_parse_numeric_label(label_text):
        text = str(label_text or "").strip()
        if not text:
            return None
        if re.fullmatch(r"\d+", text):
            try:
                return int(text)
            except ValueError:
                return None
        return None

    def _sort_statistics_rows(self, table_rows, column, order):
        """Ordena mantendo 'Total' fixo no final da lista."""
        normal_rows = [row for row in table_rows if not row.get("is_total")]
        total_rows = [row for row in table_rows if row.get("is_total")]
        reverse = order == Qt.DescendingOrder

        if column == 2:
            normal_rows.sort(key=lambda row: float(row.get("raw_value", 0.0)), reverse=reverse)
        elif column == 1:
            normal_rows.sort(key=lambda row: int(row.get("legend_order", 0)), reverse=reverse)

        return normal_rows + total_rows

    def _handle_statistics_header_click(self, table_widget, section):
        """Permite ordenacao apenas nas colunas Legenda (1) e Valor (2)."""
        if section not in (1, 2):
            return

        header = table_widget.horizontalHeader()
        if header is None:
            return

        sort_state = getattr(table_widget, "_statistics_sort_state", {"column": None, "order": None})
        current_column = sort_state.get("column")
        current_order = sort_state.get("order")
        if current_column == section:
            if current_order == Qt.AscendingOrder:
                next_order = Qt.DescendingOrder
            else:
                next_order = Qt.AscendingOrder
        else:
            next_order = Qt.AscendingOrder

        sort_state["column"] = section
        sort_state["order"] = next_order
        table_widget._statistics_sort_state = sort_state

        table_rows = list(getattr(table_widget, "_statistics_rows", []))
        sorted_rows = self._sort_statistics_rows(table_rows, section, next_order)
        table_widget._statistics_rows = sorted_rows
        self._render_statistics_table_rows(table_widget, sorted_rows)
        table_widget.resizeRowsToContents()
        header.setSortIndicator(section, next_order)

    @staticmethod
    def _copy_table_selection(table_widget):
        indexes = table_widget.selectedIndexes()
        if not indexes:
            indexes = [
                table_widget.model().index(row, col)
                for row in range(table_widget.rowCount())
                for col in range(table_widget.columnCount())
            ]
        if not indexes:
            return

        row_map = {}
        for index in indexes:
            if not index.isValid():
                continue
            row_map.setdefault(index.row(), {})[index.column()] = index.data() or ""

        lines = []
        for row in sorted(row_map.keys()):
            cols = row_map[row]
            line = "\t".join(str(cols.get(col, "")) for col in sorted(cols.keys()))
            lines.append(line)

        QApplication.clipboard().setText("\n".join(lines))

