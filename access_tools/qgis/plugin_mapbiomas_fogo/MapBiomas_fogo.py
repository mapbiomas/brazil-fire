    def unload(self):
        """Removes the plugin menu item and icon from QGIS GUI."""
        self._is_unloading = True
        self._prune_deleted_docks()
        for action in self.actions:
            self.iface.removePluginMenu(
                self.tr(u'&MapBiomas Fogo'),
                action)
            self.iface.removeToolBarIcon(action)

        for dock in list(self._stats_docks):
            try:
                if not sip.isdeleted(dock):
                    self.iface.removeDockWidget(dock)
                    dock.deleteLater()
            except RuntimeError:
                pass
        self._stats_docks = []
        self._dock_by_layer_id = {}
        self._closing_docks_from_layer.clear()

        try:
            QgsProject.instance().layerWillBeRemoved.disconnect(self._on_project_layer_will_be_removed)
        except Exception:
            pass

    def _prune_deleted_docks(self):
        """Remove referencias Python para docks ja destruidos no lado C++."""
        alive_docks = []
        for dock in self._stats_docks:
            try:
                if not sip.isdeleted(dock):
                    alive_docks.append(dock)
            except RuntimeError:
                continue
        self._stats_docks = alive_docks

        for layer_id, dock in list(self._dock_by_layer_id.items()):
            try:
                if sip.isdeleted(dock):
                    self._dock_by_layer_id.pop(layer_id, None)
            except RuntimeError:
                self._dock_by_layer_id.pop(layer_id, None)

    def _on_project_layer_will_be_removed(self, layer_id):
        if self._is_unloading:
            return

        if not isinstance(layer_id, str):
            try:
                layer_id = layer_id.id()
            except Exception:
                return

        dock = self._dock_by_layer_id.pop(layer_id, None)
        if dock is None:
            return

        self._closing_docks_from_layer.add(layer_id)
        if dock in self._stats_docks:
            self._stats_docks.remove(dock)

        try:
            if not sip.isdeleted(dock):
                self.iface.removeDockWidget(dock)
                dock.deleteLater()
        except RuntimeError:
            pass

    def _on_stats_dock_destroyed(self, layer_id):
        self._prune_deleted_docks()
        self._dock_by_layer_id.pop(layer_id, None)
        if layer_id in self._closing_docks_from_layer:
            self._closing_docks_from_layer.remove(layer_id)
            return

        if self._is_unloading:
            return

        layer = QgsProject.instance().mapLayer(layer_id)
        if layer is not None:
            QgsProject.instance().removeMapLayer(layer_id)

    @staticmethod
    def _strip_layer_index(name):
        return re.sub(r"^\s*#\s*\d+\s*-\s*", "", str(name or "")).strip()

    @staticmethod
    def _strip_temporal_suffix(name):
        """Remove sufixos temporais como 'em 2024', 'de 1985 a 2025' e 'ate 2025'."""
        text = str(name or "").strip()
        patterns = [
            r"\s+de\s+\d{4}\s+a\s+\d{4}\s*$",
            r"\s+ate\s+\d{4}\s*$",
            r"\s+até\s+\d{4}\s*$",
            r"\s+em\s+\d{4}\s*$",
            r"\s+para\s+\d{4}\s*$",
        ]
        for pattern in patterns:
            text = re.sub(pattern, "", text, flags=re.IGNORECASE)
        return re.sub(r"\s+", " ", text).strip()

