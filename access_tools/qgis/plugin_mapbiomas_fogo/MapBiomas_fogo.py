    @staticmethod
    def _resolve_selected_year_for_legend(legend_key, year, legend_year=None):
        """Resolve o ano de referencia usado para filtrar itens de legenda."""
        if legend_key == "fire_total_burned_last_year":
            return legend_year if isinstance(legend_year, int) else year if isinstance(year, int) else None

        if legend_key in {
            "fire_total_burned_accumulated",
            "fire_total_burned_frequency",
        }:
            if isinstance(year, list) and year:
                return year[-1]
            if isinstance(year, int):
                return year

        return None

    @classmethod
    def _resolve_statistics_year(cls, legend_key, year):
        """Normaliza o year esperado pelo endpoint statistics/area."""
        if legend_key == "fire_total_burned_mapbiomas_total_burned" and isinstance(year, int):
            try:
                min_year = int(ano_minimo)
            except (TypeError, ValueError):
                min_year = 1985
            return list(range(min_year, year + 1))
        return year

    @staticmethod
    def _resolve_statistics_year_series_anchor(legend_key, year):
        """Define quando a API deve retornar serie temporal no statistics/area."""
        if legend_key == "fire_total_burned_accumulated" and isinstance(year, list) and len(year) >= 2:
            return "start"
        return None

    @staticmethod
    def _format_statistics_period_label(year_values):
        """Converte o campo year da API em um rotulo legivel para a tabela."""
        if isinstance(year_values, list):
            clean_years = [str(year) for year in year_values if year is not None]
            if not clean_years:
                return "Sem periodo"
            if len(clean_years) == 1 or clean_years[0] == clean_years[-1]:
                return clean_years[0]
            return clean_years[-1]
        if year_values is None:
            return "Sem periodo"
        return str(year_values)

    def _build_statistics_request_data(self, layer):
        context_raw = layer.customProperty("firelegend/context_json", "{}")
        try:
            context = json.loads(context_raw) if context_raw else {}
        except json.JSONDecodeError:
            context = {}

        legend_key = context.get("legendKey")
        if not legend_key:
            raise ValueError("Nao foi possivel identificar legendKey da camada.")

        region = context.get("region") or "brazil"
        subtheme_key = context.get("subthemeKey")
        pixel_values = context.get("pixelValues") or []
        year = context.get("year")
        territory_ids = context.get("territoryIds")
        if isinstance(territory_ids, str):
            territory_ids_log = [territory_ids]
        elif isinstance(territory_ids, (list, tuple, set)):
            territory_ids_log = [str(item) for item in territory_ids if str(item).strip()]
        elif territory_ids is None:
            territory_ids_log = []
        else:
            territory_ids_log = [str(territory_ids)]

        if not subtheme_key:
            raise ValueError("Nao foi possivel identificar subthemeKey da camada.")

        # Para subthemas de acumulado/frequencia a API de estatisticas espera
        # apenas o ano final. Para fire_total_burned_last_year ela aceita o
        # intervalo completo e a lista de pixelValues.
        stats_year = self._resolve_statistics_year(legend_key, year)
        year_series_anchor = self._resolve_statistics_year_series_anchor(legend_key, stats_year)
        stats_pixel_values = pixel_values
        territory_category_id = 4 if legend_key == "fire_total_burned_mapbiomas_total_burned" else None
        property_code = "" if legend_key == "fire_total_burned_mapbiomas_total_burned" else None

        request_args = {
            "region": region,
            "subthemeKey": subtheme_key,
            "legendKey": legend_key,
            "pixelValue": stats_pixel_values,
            "year": stats_year,
            "territoryIds": territory_ids,
            "yearSeriesAnchor": year_series_anchor,
            "propertyCode": property_code,
            "territoryCategoryId": territory_category_id,
        }

        items_raw = layer.customProperty("firelegend/items_json", "[]")
        try:
            legend_items = json.loads(items_raw) if items_raw else []
        except json.JSONDecodeError:
            legend_items = []

        return {
            "context": context,
            "legend_items": legend_items,
            "request_args": request_args,
            "territory_ids_log": territory_ids_log,
        }

