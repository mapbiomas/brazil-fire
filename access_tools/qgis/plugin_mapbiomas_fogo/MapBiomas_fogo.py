    def _load_statistics_payload_from_request(
        self,
        request_data,
        request_timeout=30,
        request_max_attempts=1,
    ):
        context = dict((request_data or {}).get("context") or {})
        request_args = dict((request_data or {}).get("request_args") or {})
        territory_ids_log = list((request_data or {}).get("territory_ids_log") or [])

        print(
            "[MapBiomas Fogo][Estatisticas] Requisicao statistics/area -> "
            f"region={request_args.get('region')}, subthemeKey={request_args.get('subthemeKey')}, "
            f"legendKey={request_args.get('legendKey')}, year={request_args.get('year')}, "
            f"pixelValues={len(request_args.get('pixelValue') or [])}, territoryIds={territory_ids_log}, "
            f"yearSeriesAnchor={request_args.get('yearSeriesAnchor')}, "
            f"territoryCategoryId={request_args.get('territoryCategoryId')}, "
            f"timeout={request_timeout}s, maxAttempts={request_max_attempts}"
        )

        payload = self.api_client.get_area_statistics(
            requestTimeout=request_timeout,
            requestMaxAttempts=request_max_attempts,
            **request_args,
        )

        statistic_entries = payload.get("statistic") or []
        first_stat = statistic_entries[0] if statistic_entries else {}
        print(
            "[MapBiomas Fogo][Estatisticas] Resposta statistics/area -> "
            f"entries={len(statistic_entries)}, unit={payload.get('unit')}, "
            f"year={first_stat.get('year')}, total={first_stat.get('total')}"
        )

        legend_items = list((request_data or {}).get("legend_items") or [])
        return payload, legend_items, context

    def _populate_statistics_table(self, dock, layer_name, status_label, result_container, payload, legend_items, context):
        statistic_entries = payload.get("statistic") or []
        if not statistic_entries:
            raise ValueError("Arquivo de estatistica sem dados em 'statistic'.")

        stat_entry = statistic_entries[0]
        year_values = stat_entry.get("year")
        if year_values is None:
            year_values = context.get("year")

        table_widget = dock.findChild(QTableWidget, "statistics_table_widget")
