/**
 * Function to apply the mask based on biome and region.
 * @param {string} biome - Biome type.
 * @param {string} region - Region code.
 * @param {ee.Image} mask - Current mask to concatenate with.
 * @returns {ee.Image} Updated mask.
 */
function applyBiomeMask(biome, region, mask, year) {
  if (biome === 'MATA_ATLANTICA') {
    if (region === '6' || region === '7') {
      mask = mask.concat([33,31,24,40,30,23,19,20,39,41]);
    }
  }
  return mask;
}

// Exporting Landsat imagery for a given year and applying quality mosaic
function exportImage(obj) {
  var image = ee.Image(obj.id);
  var name = obj.id.split('/')[6];
  var split = name.split('-');
  var biome = split[0];
  var year = split[1];
  var region = split[2];
  var mask = masks[biome];
  //var yearAlternative = year;
  //var yearAlternative = year === '2024' ? '2023' : year;
  
  // Mask for the current year
  // if (year === '2024') {
  //   yearAlternative = '2023';
  // }

  
  mask = applyBiomeMask(biome, region, mask);
  mask = landcover
    .select(ee.String('classification_').cat(yearAlternative))
    .eq(mask)
    .reduce('sum')
    .gte(1);
  
  if ('AMAZONIA' === biome){
    // Classes de interesse
    var classesOfInterest = [33, 31, 24, 30, 23];
    
    // Iterar sobre cada classe de interesse
    classesOfInterest.forEach(function(classNumber) {
      // Criar um buffer para a classe atual
      var buffer = landcover
        .select(ee.String('classification_').cat(yearAlternative))
        .eq([classNumber])
        .reduce('sum')
        .gte(1)
        .selfMask()
        .focalMode({
          radius: 100,
          kernelType: 'square',
          units: 'meters'
        })
        .gte(1);
      
      // Mesclar o buffer com a máscara atual
      mask = mask.blend(buffer);
    });
  
  }

  // Apply 100m buffer to the class 33 only within the Pantanal lakes polygon
  if (biome === 'PANTANAL') {
    // Create a mask for class 33
    var class_33_mask = landcover
      .select(ee.String('classification_').cat(yearAlternative))
      .eq(33)
      .reduce('sum')
      .gte(1)
      .selfMask()
      .focalMode({
        radius: 100,
        kernelType: 'square',
        units: 'meters'
      })
      .gte(1)
      .clip(lakes_pantanal);
    
    // Blend the inverted mask with the current mask
    mask = mask.blend(class_33_mask);
  }

  // Remove pixels falling within the Pantanal mask
  var mask_pantanal = ee.Image(1).paint(mascara_pantanal, 0);
  mask = mask.updateMask(mask_pantanal);

  // Remove pixels based on the stable water mask
  var water_stable_mask = water_stable.unmask().neq(1);
  var image_mask = image.updateMask(water_stable_mask);


  // Remove pixels based on the final mask
  var final_mask = mask.neq(1);
  image_mask = image_mask.updateMask(final_mask);

  
  //////////////////////////////////////////////////////
  // Remove solitary pixels
  // Compute the focal model
    
  // Compute te number of connections
  var connections = image_mask.connectedPixelCount({'maxSize': 100, 'eightConnected': false});
  
  // Definir tamanho do filtro condicionalmente
  //var filter_size = (biome === 'PAMPA') ? 2 : 6;
  //var filter_size = 2;//Pampa 
  var filter_size = 6;//Demais

  // Identificar pixels solitários
  var solitary_pixels = connections.lte(filter_size);
  
  // Aplicar a remoção dos pixels solitários
  image_mask = image_mask.where(solitary_pixels, 0).selfMask().reproject('EPSG:4326', null, 30);

  // Map.addLayer(solitary_pixels,{palette:['cccccc','ffff00']},'solitary_pixels',false);
  
  //////////////////////////////////////////////////////
  
  Map.addLayer(landcover.select(ee.String('classification_').cat(yearAlternative)),
  {bands: ['classification_' + yearAlternative],min: 0,max: 62,
    palette: require('users/mapbiomas/modules:Palettes.js').get('classification9')},
  'landcover ' + yearAlternative,false);
  Map.addLayer(image,{'min':0,'max':1,'palette': ['000000']},'image-no-mask ' + year);
  Map.addLayer(image_mask, {'min':0,'max':1,'palette': ['ff0000']}, 'Image with Masks');
  Map.addLayer(final_mask, {}, 'Final Mask',false);
  
  ee.Number.parse(year).int().evaluate(function(y) {
    var qualityMosaic = collection_landsat.landsat_year(y, geometry)
      .qualityMosaic('nbr')
      .select('monthOfYear')
      .byte();

    var startDate = ee.Date.fromYMD(y, 1, 1);
    var endDate = ee.Date.fromYMD(y + 1, 1, 1);

    var properties = {
      'source': 'IPAM',
      'pixel_unit': 'month',
      'name': name,
      'year': y,
      'region': region,
      'biome': biome,
      "system:time_start": startDate.millis(),
      "system:time_end": endDate.millis()
    };

    var image_final = qualityMosaic
      .updateMask(image_mask)
      .set(properties);

    Map.addLayer(image_final,{min:1,max:12,palette:['ff0000','00ff00','0000ff']},'image final (with months) ' + name,false);

    Export.image.toAsset({
      image: image_final,
      description: name,
      assetId: collection_output + '/' + name,
      pyramidingPolicy: 'mode',
      region: geometry,
      scale: 30,
      maxPixels: 1e13,
    });
  });
}

